    private AbstractTableModel criarModeloTabela() { 
       String[] nomeColunas = {"Nome", "CÃ³digo", "fabricante"};


        return new AbstractTableModel() {
            @Override
            public String getColumnName(int column) {
                return nomeColunas[column];
            }

            @Override
            public int getColumnCount() {
                return nomeColunas.length;
            }

            @Override
            public int getRowCount() {
                return sistema.getListaVacinas().size();
            }



            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {

                switch (columnIndex) {
                    case 0: 
                        return sistema.getListaVacinas().todos().get(rowIndex).getNome();
                    case 1:
                        return sistema.getListaVacinas().todos().get(rowIndex).getCodigo();
                    case 2:
                        return sistema.getListaVacinas().todos().get(rowIndex).getFabricante();

                    default:
                        return "";
                }
            }
        };
    }
    
    public void escolherVacina()
    {
        
           int rowIndex = tableVacinas.getSelectedRow();
           if (rowIndex == -1) return;
           String nome = (String) tableModel.getValueAt(rowIndex, 0);
           Vacina vacina = sistema.getListaVacinas().getVacina(nome);
           AlocarVacinas av = new AlocarVacinas(sistema,serializacao, centro, vacina);
           av.setVisible(true);
           dispose();
    }
    
